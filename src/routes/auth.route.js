import express from "express"
import { authGetmeController, authGoogleLoginController, authLoginController, authRegisterController, authUpdateProfileController,authVerifyEmailConroller, authGetPublicProfileController } from "../controllers/auth.controller.js"
import authUserCheck from "../middlewares/userAuthen.middleware.js"
const authRoute = express.Router()

/**
 * @swagger
 * /api/auth/profile/{id}:
 *   get:
 *     summary: Get public user profile by ID
 *     tags: [Auth]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     responses:
 *       200:
 *         description: Public profile retrieved
 *       404:
 *         description: User not found
 */
authRoute.get('/profile/:id', authUserCheck, authGetPublicProfileController)

/**
 * @swagger
 * /api/auth/register:
 *   post:
 *     summary: Register a new user
 *     tags: [Auth]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *               - confirmpassword
 *               - name
 *             properties:
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *               confirmPassword:
 *                 type: string
 *               name:
 *                 type: string
 *     responses:
 *       201:
 *         description: User registered successfully
 *       400:
 *         description: Bad request
 */
authRoute.post('/register', authRegisterController)

/**
 * @swagger
 * /api/auth/login:
 *   post:
 *     summary: User login
 *     tags: [Auth]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       200:
 *         description: Login successful
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 token:
 *                   type: string
 *       401:
 *         description: Unauthorized
 */
authRoute.post('/login', authLoginController)

/**
 * @swagger
 * /api/auth/me:
 *   get:
 *     summary: Get current user profile
 *     tags: [Auth]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: User profile retrieved
 *       401:
 *         description: Unauthorized
 */
authRoute.get('/me', authUserCheck, authGetmeController)

/**
 * @swagger
 * /api/auth/profile:
 *   put:
 *     summary: Update current user profile (name, avatarUrl)
 *     tags: [Auth]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               avatarUrl:
 *                 type: string
 *     responses:
 *       200:
 *         description: Profile updated successfully
 *       400:
 *         description: Bad request
 *       401:
 *         description: Unauthorized
 */
authRoute.put('/profile', authUserCheck, authUpdateProfileController)


authRoute.post('/google', authGoogleLoginController)
authRoute.get("/verify-email",authVerifyEmailConroller);
export default authRoute